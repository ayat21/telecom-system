create policy "actions_select" on public.actions for select using (true);

drop policy if exists "actions_insert" on public.actions;
create policy "actions_insert" on public.actions for insert with check (true);

drop policy if exists "actions_update" on public.actions;
create policy "actions_update" on public.actions for update using (true);

drop policy if exists "actions_delete" on public.actions;
create policy "actions_delete" on public.actions for delete using (true);

drop policy if exists "notifications_select" on public.notifications;
create policy "notifications_select" on public.notifications for select using (true);

drop policy if exists "notifications_insert" on public.notifications;
create policy "notifications_insert" on public.notifications for insert with check (true);

drop policy if exists "notifications_update" on public.notifications;
create policy "notifications_update" on public.notifications for update using (true);

drop policy if exists "notifications_delete" on public.notifications;
create policy "notifications_delete" on public.notifications for delete using (true);
